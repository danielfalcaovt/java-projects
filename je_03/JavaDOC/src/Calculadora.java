public class Calculadora {
    /**
    * @param num1  argumento do tipo numero do método sum
    * @param num2  argumento do tipo numero do método sum
    * @return      O resultado da soma entre num1 e num2
     * @since v1.0
    * */
    Integer sum(int num1, int num2) {
        return num1 + num2;
    }
}
